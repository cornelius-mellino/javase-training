public class IfElse {
    
    public static void main(String[] args) {
        int val = 2;
        
        if (val > 0) {
            System.out.println("If - true");
        }
        
        if (val < 10) {
            System.out.println("If-Else - true");
        }
        else {
            System.out.println("If-Else - false");
        }
        
    }
}
